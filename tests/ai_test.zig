const std = @import("std");
const helpers = @import("helpers.zig");
const runZr = helpers.runZr;

test "ai: build query" {
    const allocator = std.testing.allocator;
    var tmp = std.testing.tmpDir(.{});
    defer tmp.cleanup();

    const tmp_path = try tmp.dir.realpathAlloc(allocator, ".");
    defer allocator.free(tmp_path);

    var result = try runZr(allocator, &[_][]const u8{ "ai", "build" }, tmp_path);
    defer result.deinit();

    try std.testing.expectEqual(@as(u8, 0), result.exit_code);
    try std.testing.expect(std.mem.indexOf(u8, result.stdout, "zr run build") != null);
    try std.testing.expect(std.mem.indexOf(u8, result.stdout, "Command translated") != null);
}


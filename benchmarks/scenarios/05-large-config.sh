#!/usr/bin/env bash
# Benchmark: Large Config Parsing Performance
# Measures time to parse and load large configuration files
# Scenario: 500 task definitions with deps, env, conditions

set -euo pipefail

BENCHMARK_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESULTS_DIR="${BENCHMARK_DIR}/../results"
mkdir -p "${RESULTS_DIR}"

ITERATIONS="${BENCH_ITERATIONS:-30}"
WARMUP="${BENCH_WARMUP:-5}"
TASK_COUNT=500

echo "=== Large Config Benchmark ==="
echo "Iterations: ${ITERATIONS}"
echo "Warmup: ${WARMUP}"
echo "Task count: ${TASK_COUNT}"
echo ""

# Create test project
TEST_DIR=$(mktemp -d)
trap "rm -rf ${TEST_DIR}" EXIT
cd "${TEST_DIR}"

# Generate large zr.toml
cat > zr.toml <<'EOF'
# Large configuration with 500 tasks
[global]
max_workers = 4

EOF

for ((i=1; i<=${TASK_COUNT}; i++)); do
  DEP_TASK=$((i - 1))
  cat >> zr.toml <<EOF
[tasks.task_${i}]
cmd = "echo task_${i}"
description = "Autogenerated task ${i} for benchmarking"
tags = ["bench", "auto", "group_$((i % 10))"]
env = { TASK_ID = "${i}", GROUP = "$((i % 10))" }
EOF
  if [ $i -gt 1 ]; then
    echo "deps = [\"task_${DEP_TASK}\"]" >> zr.toml
  fi
  echo "" >> zr.toml
done

# Generate large Makefile
cat > Makefile <<'EOF'
.PHONY: task_1
task_1:
	@echo task_1

EOF

for ((i=2; i<=${TASK_COUNT}; i++)); do
  DEP_TASK=$((i - 1))
  cat >> Makefile <<EOF
.PHONY: task_${i}
task_${i}: task_${DEP_TASK}
	@echo task_${i}

EOF
done

# Generate large Justfile
cat > Justfile <<'EOF'
task_1:
	echo task_1

EOF

for ((i=2; i<=${TASK_COUNT}; i++)); do
  DEP_TASK=$((i - 1))
  cat >> Justfile <<EOF
task_${i}: task_${DEP_TASK}
	echo task_${i}

EOF
done

# Generate large Taskfile.yml
cat > Taskfile.yml <<'EOF'
version: '3'
tasks:
  task_1:
    cmds:
      - echo task_1
EOF

for ((i=2; i<=${TASK_COUNT}; i++)); do
  DEP_TASK=$((i - 1))
  cat >> Taskfile.yml <<EOF
  task_${i}:
    deps:
      - task_${DEP_TASK}
    cmds:
      - echo task_${i}
EOF
done

# Generate large package.json
cat > package.json <<'EOF'
{
  "scripts": {
    "task_1": "echo task_1"
EOF

for ((i=2; i<=${TASK_COUNT}; i++)); do
  if [ $i -eq ${TASK_COUNT} ]; then
    echo "    \"task_${i}\": \"echo task_${i}\"" >> package.json
  else
    echo "    \"task_${i}\": \"echo task_${i}\"," >> package.json
  fi
done

cat >> package.json <<'EOF'
  }
}
EOF

# Measure zr (list all tasks to force full config parse)
echo "Benchmarking zr config parsing..."
ZR_TIMES=()
for ((i=1; i<=${WARMUP}+${ITERATIONS}; i++)); do
  START=$(date +%s%N)
  zr list >/dev/null 2>&1
  END=$(date +%s%N)
  ELAPSED=$(( (END - START) / 1000000 )) # Convert to milliseconds
  if [ $i -gt ${WARMUP} ]; then
    ZR_TIMES+=($ELAPSED)
  fi
done

# Measure make (list all targets)
echo "Benchmarking make config parsing..."
MAKE_TIMES=()
for ((i=1; i<=${WARMUP}+${ITERATIONS}; i++)); do
  START=$(date +%s%N)
  make -qp 2>/dev/null | grep "^task_" | wc -l >/dev/null
  END=$(date +%s%N)
  ELAPSED=$(( (END - START) / 1000000 ))
  if [ $i -gt ${WARMUP} ]; then
    MAKE_TIMES+=($ELAPSED)
  fi
done

# Measure just (list all recipes)
JUST_TIMES=()
if command -v just &> /dev/null; then
  echo "Benchmarking just config parsing..."
  for ((i=1; i<=${WARMUP}+${ITERATIONS}; i++)); do
    START=$(date +%s%N)
    just --list >/dev/null 2>&1
    END=$(date +%s%N)
    ELAPSED=$(( (END - START) / 1000000 ))
    if [ $i -gt ${WARMUP} ]; then
      JUST_TIMES+=($ELAPSED)
    fi
  done
fi

# Measure task (list all tasks)
TASK_TIMES=()
if command -v task &> /dev/null; then
  echo "Benchmarking task config parsing..."
  for ((i=1; i<=${WARMUP}+${ITERATIONS}; i++)); do
    START=$(date +%s%N)
    task --list >/dev/null 2>&1
    END=$(date +%s%N)
    ELAPSED=$(( (END - START) / 1000000 ))
    if [ $i -gt ${WARMUP} ]; then
      TASK_TIMES+=($ELAPSED)
    fi
  done
fi

# Measure npm (no native list command, parse package.json)
NPM_TIMES=()
if command -v npm &> /dev/null; then
  echo "Benchmarking npm config parsing..."
  for ((i=1; i<=${WARMUP}+${ITERATIONS}; i++)); do
    START=$(date +%s%N)
    npm run >/dev/null 2>&1
    END=$(date +%s%N)
    ELAPSED=$(( (END - START) / 1000000 ))
    if [ $i -gt ${WARMUP} ]; then
      NPM_TIMES+=($ELAPSED)
    fi
  done
fi

# Calculate statistics
calc_stats() {
  local arr=("$@")
  local sum=0
  local min=${arr[0]}
  local max=${arr[0]}

  for val in "${arr[@]}"; do
    sum=$((sum + val))
    [ $val -lt $min ] && min=$val
    [ $val -gt $max ] && max=$val
  done

  local mean=$((sum / ${#arr[@]}))

  # Calculate median
  IFS=$'\n' sorted=($(sort -n <<<"${arr[*]}"))
  unset IFS
  local mid=$((${#sorted[@]} / 2))
  local median=${sorted[$mid]}

  echo "$mean,$median,$min,$max"
}

# Output results
TIMESTAMP=$(date +%Y%m%d_%H%M%S)
RESULTS_FILE="${RESULTS_DIR}/05-large-config_${TIMESTAMP}.csv"

echo "tool,mean_ms,median_ms,min_ms,max_ms,task_count" > "$RESULTS_FILE"

ZR_STATS=$(calc_stats "${ZR_TIMES[@]}")
echo "zr,$ZR_STATS,${TASK_COUNT}" >> "$RESULTS_FILE"

MAKE_STATS=$(calc_stats "${MAKE_TIMES[@]}")
echo "make,$MAKE_STATS,${TASK_COUNT}" >> "$RESULTS_FILE"

if [ ${#JUST_TIMES[@]} -gt 0 ]; then
  JUST_STATS=$(calc_stats "${JUST_TIMES[@]}")
  echo "just,$JUST_STATS,${TASK_COUNT}" >> "$RESULTS_FILE"
fi

if [ ${#TASK_TIMES[@]} -gt 0 ]; then
  TASK_STATS=$(calc_stats "${TASK_TIMES[@]}")
  echo "task,$TASK_STATS,${TASK_COUNT}" >> "$RESULTS_FILE"
fi

if [ ${#NPM_TIMES[@]} -gt 0 ]; then
  NPM_STATS=$(calc_stats "${NPM_TIMES[@]}")
  echo "npm,$NPM_STATS,${TASK_COUNT}" >> "$RESULTS_FILE"
fi

echo ""
echo "=== Results ==="
cat "$RESULTS_FILE"
echo ""
echo "Results saved to: $RESULTS_FILE"
echo ""
echo "Config file sizes:"
ls -lh zr.toml Makefile Justfile Taskfile.yml package.json 2>/dev/null | awk '{print $9, $5}'
